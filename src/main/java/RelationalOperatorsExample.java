public class RelationalOperatorsExample {
    public RelationalOperatorsExample( ) {

        int x = 10, y = 5;
        System.out.println ("x = " + x + " y = " + y);
        System.out.println("x > y : "+(x > y));
        System.out.println("x < y : "+(x < y));
        System.out.println("x >= y : "+(x >= y));
        System.out.println("x <= y : "+(x <= y));
        System.out.println("x == y : "+(x == y));
        System.out.println("x != y : "+(x != y));
    }

    public static void main(String args[]){
        new RelationalOperatorsExample();
    }
}